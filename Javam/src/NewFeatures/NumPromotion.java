package NewFeatures;

public class NumPromotion {
	public static void main(String[] args) {
		double d = 1.2;
		float f = 1.2f;
		float f2 = 1.2f;
		System.out.println(d + f);
		System.out.println(f+f2);
		int a = 1;
		System.out.println("///////");
		System.out.println(d+a);
		System.out.println(f+a);
		System.out.println("///////");
		int i = 1;
		System.out.println(i+  " " +i);
	}
}
